namespace nystudio107\seomatic\models\jsonld;

/**
 * schema.org version: v14.0-release
 * Trait for Service.
 *
 * @author    nystudio107
 * @package   Seomatic
 * @see       https://schema.org/Service
 */

trait ServiceTrait
{
    
    /**
     * A review of the item.
     *
     * @var Review
     */
    public $review;

    /**
     * An award won by or for this item.
     *
     * @var string|Text
     */
    public $award;

    /**
     * The audience eligible for this service.
     *
     * @var Audience
     */
    public $serviceAudience;

    /**
     * A category for the item. Greater signs or slashes can be used to informally
     * indicate a category hierarchy.
     *
     * @var string|URL|Text|PhysicalActivityCategory|Thing|CategoryCode
     */
    public $category;

    /**
     * The hours during which this service or contact is available.
     *
     * @var OpeningHoursSpecification
     */
    public $hoursAvailable;

    /**
     * An intended audience, i.e. a group for whom something was created.
     *
     * @var Audience
     */
    public $audience;

    /**
     * An offer to provide this item—for example, an offer to sell a product,
     * rent the DVD of a movie, perform a service, or give away tickets to an
     * event. Use [[businessFunction]] to indicate the kind of transaction
     * offered, i.e. sell, lease, etc. This property can also be used to describe
     * a [[Demand]]. While this property is listed as expected on a number of
     * common types, it can be used in others. In that case, using a second type,
     * such as Product or a subtype of Product, can clarify the nature of the
     * offer.       
     *
     * @var Offer|Demand
     */
    public $offers;

    /**
     * The tangible thing generated by the service, e.g. a passport, permit, etc.
     *
     * @var Thing
     */
    public $serviceOutput;

    /**
     * The service provider, service operator, or service performer; the goods
     * producer. Another party (a seller) may offer those services or goods on
     * behalf of the provider. A provider may also serve as the seller.
     *
     * @var Organization|Person
     */
    public $provider;

    /**
     * Human-readable terms of service documentation.
     *
     * @var string|URL|Text
     */
    public $termsOfService;

    /**
     * Indicates the mobility of a provided service (e.g. 'static', 'dynamic').
     *
     * @var string|Text
     */
    public $providerMobility;

    /**
     * An entity that arranges for an exchange between a buyer and a seller.  In
     * most cases a broker never acquires or releases ownership of a product or
     * service involved in an exchange.  If it is not clear whether an entity is a
     * broker, seller, or buyer, the latter two terms are preferred.
     *
     * @var Person|Organization
     */
    public $broker;

    /**
     * The overall rating, based on a collection of reviews or ratings, of the
     * item.
     *
     * @var AggregateRating
     */
    public $aggregateRating;

    /**
     * A pointer to another, functionally similar product (or multiple products).
     *
     * @var Service|Product
     */
    public $isSimilarTo;

    /**
     * A means of accessing the service (e.g. a phone bank, a web site, a
     * location, etc.).
     *
     * @var ServiceChannel
     */
    public $availableChannel;

    /**
     * A slogan or motto associated with the item.
     *
     * @var string|Text
     */
    public $slogan;

    /**
     * The brand(s) associated with a product or service, or the brand(s)
     * maintained by an organization or business person.
     *
     * @var Organization|Brand
     */
    public $brand;

    /**
     * An associated logo.
     *
     * @var URL|ImageObject
     */
    public $logo;

    /**
     * The tangible thing generated by the service, e.g. a passport, permit, etc.
     *
     * @var Thing
     */
    public $produces;

    /**
     * The geographic area where the service is provided.
     *
     * @var GeoShape|AdministrativeArea|Place
     */
    public $serviceArea;

    /**
     * The type of service being offered, e.g. veterans' benefits, emergency
     * relief, etc.
     *
     * @var string|GovernmentBenefitsType|Text
     */
    public $serviceType;

    /**
     * The geographic area where a service or offered item is provided.
     *
     * @var string|AdministrativeArea|GeoShape|Text|Place
     */
    public $areaServed;

    /**
     * A pointer to another, somehow related product (or multiple products).
     *
     * @var Service|Product
     */
    public $isRelatedTo;

    /**
     * Indicates an OfferCatalog listing for this Organization, Person, or
     * Service.
     *
     * @var OfferCatalog
     */
    public $hasOfferCatalog;

}
